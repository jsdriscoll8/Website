            <section class="Environmental benefits">
                <h2>Environmental Benefits of Green Energy</h2>
                <p>
                    Beyond reducing emissions, there are further environmental benefits of green energy.
                    In addition to greenhouse gases, fossil fuel burning often results in extremely
                    harmful, difficult to dispose byproducts, such as coal sludge and fracking waste.
                    Often, these substances find their ways into the natural environment, harming ecosystems
                    and dirtying water supplies. Green energy mostly avoids similar outputs. 
                </p>
            </section>
            
            <section class="Cost benefits">
                <h2>Cost Benefits of Green Energy</h2>
                <p>
                    While fossil fuels are very energy dense for the cost needed to extract them, green energy
                    offers often comparable or better price benefits. Onshore wind, hydropower, and solar power
                    offer similar, if not better, cost per kilowatt-hour. When factoring in the costs associated
                    with climate change, green energy has a strong financial argument. 
                    <cite> Source: Our World in Data </cite>
                </p>
