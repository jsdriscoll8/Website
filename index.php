            <section class="Drawbacks of traditional energy">
                <h2>Drawbacks of Traditional Energy</h2>
                <p>
                    As aforementioned, the most significant drawback to traditional, fossil-fuel energy sources
                    are the greenhouse gas emissions they produce. The same chemical reactions that occur when
                    these fuels (coal, oil, natural gas, etc.) are burnt to generate electricity also release 
                    carbon dioxide, methane, and other warming gases. While energy-dense, relatively cheap, and
                    plentifully available, fossil fuels heat the earth, thereby changing the climate.
                </p>
