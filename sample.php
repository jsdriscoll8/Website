        <h1> How to Play Civ VI </h1> 
        <p> Civilization is an immensely complex game. There are a lot of systems and concepts to keep in mind, a multitude of victory routes, and of course 
        many other civilizations vying for victory against you. You must have adequate science, culture, military power, and diplomacy to succeed,
        but attempting to maintain them all simultaneously will likely lead to your downfall. You must construct functional, productive, happy cities;
        understand when to go to war; and have a knowledge of defensive tactics for when others attack you. While this may seem overbearing, Civilization VI 
        provides a multitude of difficulty options and gamemodes to ease in new players. Further, for as many ways as there are to play the game incorrectly,
        it is usually possible to change course and come out on top. Altogether, "Civ" is a worthwhile strategic experience, and its initially overbearing complexity
        hides an excellent experience.
